CREATE TABLE USUARIO(
    CODIGO_USUARIO NUMBER(6) CONSTRAINT CODIGO_USUARIO_PK PRIMARY KEY,
    NOMBRE_USUARIO VARCHAR2(30) CONSTRAINT NOMBRE_USUARIO_NN NOT NULL,
    APELLIDOS VARCHAR2(70),
    MAIL VARCHAR2(100),
    TELEFONO VARCHAR2(12),
    TIPO_USUARIO VARCHAR2(30) CONSTRAINT TIPO_USUARIO_NN NOT NULL
);

CREATE TABLE PROVEEDOR(
    CIF VARCHAR2(9) CONSTRAINT CIF_PROVEEDOR_PK PRIMARY KEY,
    NOMBRE_PROVEEDOR VARCHAR2(50),
    DIRECCION VARCHAR2(150),
    CIUDAD VARCHAR2(30),
    CP NUMBER(5),
    TELEFONO VARCHAR2(12),
    MAIL VARCHAR2(100)
);

CREATE TABLE MATERIAL(
    CODIGO_MATERIAL NUMBER(6) CONSTRAINT CODIGO_MATERIAL_PK PRIMARY KEY,
    NOMBRE_MATERIAL VARCHAR2(70),
    TIPO_MATERIAL VARCHAR2(30),
    DESCRIPCION VARCHAR2(200),
    STOCK NUMBER(5)
);

CREATE TABLE PROYECTO(
    CODIGO_PROYECTO NUMBER(6) CONSTRAINT CODIGO_PROYECTO_PK PRIMARY KEY,
    NOMBRE_PROYECTO VARCHAR2(70),
    DESCRIPCION VARCHAR2(200)
);

CREATE TABLE EVENTO(
    CODIGO_EVENTO NUMBER(6) CONSTRAINT CODIGO_EVENTO_PK PRIMARY KEY,
    MENTOR VARCHAR2(30) NOT NULL,
    TIPO_EVENTO VARCHAR2(30),
    DESCRIPCION VARCHAR2(200),
    FECHA_INICIO DATE,
    FECHA_FIN DATE,
    LUGAR VARCHAR2(50)
);

CREATE TABLE MAQUINA(
    CODIGO_MAQUINA NUMBER(6) CONSTRAINT CODIGO_MAQUINA_PK PRIMARY KEY,
    NOMBRE_MAQUINA VARCHAR2(50),
    DESCRIPCION VARCHAR2(200),
    DISPONIBILIDAD VARCHAR2(30) DEFAULT 'DISPONIBLE'
);

CREATE TABLE PEDIDO(
    CODIGO_PEDIDO NUMBER(6) CONSTRAINT CODIGO_PEDIDO_PK PRIMARY KEY,
    FECHA DATE,
    PRECIO_TOTAL NUMBER(7,2),
    CIF_PROVEEDOR VARCHAR2(9),
    DESCRIPCION VARCHAR2(400),
    CONSTRAINT CIF_PROVEEDOR_FK FOREIGN KEY(CIF_PROVEEDOR) REFERENCES PROVEEDOR(CIF) ON DELETE SET NULL
);

CREATE TABLE LINEA_PEDIDO(
    CODIGO_PEDIDO NUMBER(6),
    CODIGO_MATERIAL NUMBER(6),
    CANTIDAD NUMBER(5),
    PRECIO NUMBER(7,2),
    CONSTRAINT CODIGO_LINEA_PEDIDO_PK PRIMARY KEY (CODIGO_PEDIDO,CODIGO_MATERIAL),
    CONSTRAINT CODIGO_PEDIDO_LINEA_FK FOREIGN KEY(CODIGO_PEDIDO) REFERENCES PEDIDO(CODIGO_PEDIDO) ON DELETE CASCADE,
    CONSTRAINT CODIGO_MATERIAL_FK FOREIGN KEY(CODIGO_MATERIAL) REFERENCES MATERIAL(CODIGO_MATERIAL) ON DELETE CASCADE
);

CREATE TABLE USA(
    CODIGO_PROYECTO NUMBER(6),
    CODIGO_MATERIAL NUMBER(6),
    CANTIDAD NUMBER(5),
    CONSTRAINT CODIGO_USA_PK PRIMARY KEY (CODIGO_PROYECTO,CODIGO_MATERIAL),
    CONSTRAINT CODIGO_PROYECTO_USA_FK FOREIGN KEY(CODIGO_PROYECTO) REFERENCES PROYECTO(CODIGO_PROYECTO) ON DELETE CASCADE,
    CONSTRAINT CODIGO_MATERIAL_USA_FK FOREIGN KEY(CODIGO_MATERIAL) REFERENCES MATERIAL(CODIGO_MATERIAL) ON DELETE CASCADE
);

CREATE TABLE RESERVA(
    CODIGO_PROYECTO NUMBER(6),
    CODIGO_MAQUINA NUMBER(6),
    CODIGO_USUARIO NUMBER(6),
    FECHA_INICIO DATE,
    FECHA_FIN DATE,
    NOMBRE_RESPONSABLE VARCHAR2(45),
    NOMBRE_MAQUINA VARCHAR2(25),
    NOMBRE_PROYECTO VARCHAR2(25),
    CONSTRAINT CODIGO_RESERVA_PK PRIMARY KEY (CODIGO_PROYECTO,CODIGO_MAQUINA, FECHA_INICIO, CODIGO_USUARIO),
    CONSTRAINT CODIGO_PROYECTO_RESERVA_FK FOREIGN KEY(CODIGO_PROYECTO) REFERENCES PROYECTO(CODIGO_PROYECTO) ON DELETE CASCADE,
    CONSTRAINT CODIGO_MAQUINA_RESERVA_FK FOREIGN KEY(CODIGO_MAQUINA) REFERENCES MAQUINA(CODIGO_MAQUINA) ON DELETE CASCADE,
     CONSTRAINT CODIGO_USUARIO_RESERVA_FK FOREIGN KEY(CODIGO_USUARIO) REFERENCES USUARIO(CODIGO_USUARIO) ON DELETE CASCADE
);

CREATE TABLE PLANIFICA(
    CODIGO_USUARIO NUMBER(6),
    CODIGO_PROYECTO NUMBER(6),
    CONSTRAINT CODIGO_PLANIFICA_PK PRIMARY KEY (CODIGO_USUARIO,CODIGO_PROYECTO),
    CONSTRAINT CODIGO_USUARIO_PLANIFICA_FK FOREIGN KEY(CODIGO_USUARIO) REFERENCES USUARIO(CODIGO_USUARIO) ON DELETE CASCADE,
    CONSTRAINT CODIGO_PROYECTO_PLANIFICA_FK FOREIGN KEY(CODIGO_PROYECTO) REFERENCES PROYECTO(CODIGO_PROYECTO) ON DELETE CASCADE  
);

CREATE TABLE ASISTE(
    CODIGO_USUARIO NUMBER(6),
    CODIGO_EVENTO NUMBER(6),
    CONSTRAINT CODIGO_ASISTE_PK PRIMARY KEY (CODIGO_USUARIO,CODIGO_EVENTO),
    CONSTRAINT CODIGO_USUARIO_ASISTE_FK FOREIGN KEY(CODIGO_USUARIO) REFERENCES USUARIO(CODIGO_USUARIO) ON DELETE CASCADE,
    CONSTRAINT CODIGO_EVENTO_ASISTE_FK FOREIGN KEY(CODIGO_EVENTO) REFERENCES EVENTO(CODIGO_EVENTO) ON DELETE CASCADE  
);

